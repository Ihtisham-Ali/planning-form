// Function to search LPAs
function searchLPAs(query) {
    if (!query || query.trim().length < 2) {
        return [];
    }

    const searchTerm = query.toLowerCase().trim();
    return UK_LPAS.filter(lpa =>
        lpa.name.toLowerCase().includes(searchTerm) ||
        lpa.region.toLowerCase().includes(searchTerm)
    ).slice(0, 50); // Limit to 50 results for performance
}

// Function to get LPA by name (exact match)
function getLPAByName(name) {
    return UK_LPAS.find(lpa => lpa.name.toLowerCase() === name.toLowerCase());
}

// Function to get LPA by ID
function getLPAById(id) {
    return UK_LPAS.find(lpa => lpa.id === id);
}

